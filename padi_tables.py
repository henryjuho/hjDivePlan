def pressure_after_time(start_p, surf_time):
    surf_time *= 60
    if surf_time >= 3*60*60:
        return 'A'
    intervals = surface_table[start_p]
    for x in intervals.keys():
        interval = intervals[x]
        if time2secs(interval[0]) <= surf_time <= time2secs(interval[1]):
            return x


